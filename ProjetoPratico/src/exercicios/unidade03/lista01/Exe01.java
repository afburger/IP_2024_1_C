package exercicios.unidade03.lista01;

public class Exe01 {

	double numero1;
	double numero2;
	
	public double soma( ) {
		return numero1 + numero2;
	}
	
	public double subtracao() {
		return numero1 - numero2;
	}
	
	public double divisao() {
		return numero1 / numero2;
	}

	public double multiplicacao() {
		return numero1 * numero2;
	}
}
